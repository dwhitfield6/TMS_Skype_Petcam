/* InitTIMERS
 *
 * The function initializes the timers.										  */
/******************************************************************************/
void InitTIMERS(void)
{
	/* set up timer interrupts */
	SYS_Unlock();
	PieVectTable.TIMER0_INT = &ISR_TIMER0_DELAY;
	PieVectTable.TIMER1_INT = &ISR_TIMER1_IR_RECEIVE;
	SYS_Lock();


}

