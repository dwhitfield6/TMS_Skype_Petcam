/******************************************************************************/
/* BLUE_ATCommand
 *
 * The function sends an AT command.										  */
/******************************************************************************/
void BLUE_ATCommand(unsigned char* command)
{
	while(*command)
	{
		UART_SendCharC(*command);
		MSC_DelayUS(10000);
		command++;
	}
}

