/******************************************************************************/
/* InitLEDs
 *
 * The function initializes the LEDs.										  */
/******************************************************************************/
void InitLEDs(void)
{
	unsigned char i;

	/* flash LEDs for user */
	for(i=0;i<10;i++)
	{
		LED_RedLED(ON);
		LED_GreenLED(OFF);
		MSC_DelayNOP(10000000);
		LED_RedLED(OFF);
		LED_GreenLED(ON);
		MSC_DelayNOP(10000000);
	}
	LED_GreenLED(OFF);
}

/******************************************************************************/
/* LED_RedLED
 *
 * The function controls the Red LED.										  */
/******************************************************************************/
void LED_RedLED(unsigned char state)
{
	if(state)
	{
		GPIO_WritePin(RED_LED_GPIO, ON);
	}
	else
	{
		GPIO_WritePin(RED_LED_GPIO, OFF);
	}
}

/******************************************************************************/
/* LED_GreenLED
 *
 * The function controls the Green LED.										  */
/******************************************************************************/
void LED_GreenLED(unsigned char state)
{
	if(state)
	{
		GPIO_WritePin(GREEN_LED_GPIO, ON);
	}
	else
	{
		GPIO_WritePin(GREEN_LED_GPIO, OFF);
	}
}

